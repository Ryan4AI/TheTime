function getKey(year, dynasty) {
  return String(year) + '|' + dynasty
}

exports.main = async (event, context) => {
  const all = await db.collection('era_meta').limit(1000).get()
  const docs = all.data
  let updated = 0
  for (const doc of docs) {
    const year = doc.year
    const dynasty = doc.dynasty
    const k = getKey(year, dynasty)
    const info = ERA_MAP[k]
    if (!info) continue
    const [eraName, eraStart] = info
    if (doc.eraName === eraName && doc.eraStart === eraStart) continue
    await db.collection('era_meta').doc(doc._id).update({
      data: { eraName, eraStart }
    })
    updated++
  }
  return { success: true, updated, total: docs.length }
}