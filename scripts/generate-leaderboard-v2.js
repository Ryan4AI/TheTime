// 计算综合分并排序
function processLeaderboard(name, config) {
  const { formula, characters } = config;
  
  // 计算综合分
  const processed = characters.map(char => {
    const score = formula(char);
    return {
      ...char,
      综合分: Math.round(score),
    };
  });
  
  // 按综合分降序排序
  processed.sort((a, b) => b.综合分 - a.综合分);
  
  // 添加排名
  return processed.map((char, index) => ({
    排名: index + 1,
    ...char,
  }));
}

