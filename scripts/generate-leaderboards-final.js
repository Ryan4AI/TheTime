// 简化版：生成100人的函数
function generate100People(baseList, formula) {
  // 如果基础列表不足100人，用简化数据补充
  while (baseList.length < 100) {
    const idx = baseList.length + 1;
    baseList.push({
      name: `历史人物${idx}`,
      dynasty: '未知',
      ...formula(idx),
      介绍: {
        title: `人物${idx}`,
        bio: `历史人物${idx}的简介`,
        achievement: `主要成就`,
        quote: `名言`,
        上榜理由: `上榜理由`
      }
    });
  }
  return baseList;
}

// 计算综合分
function processLeaderboard(name, characters, formula) {
  const processed = characters.map(char => ({
    ...char,
    综合分: Math.round(formula(char))
  }));
  processed.sort((a, b) => b.综合分 - a.综合分);
  return processed.map((char, index) => ({ 排名: index + 1, ...char }));
}

